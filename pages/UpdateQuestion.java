    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        String id=qid.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection con=DriverManager.getConnection("jdbc:mysql://localhost/quizgame","root","1234");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from questions where qid= '"+id+"' ");
            
            if(rs.first()){
                questionField.setText(rs.getString(2));
                option1.setText(rs.getString(3));
                option2.setText(rs.getString(4));
                option3.setText(rs.getString(4));
                option4.setText(rs.getString(5));
                ans.setText(rs.getString(6));
                qid.setEditable(false);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchbtnActionPerformed

