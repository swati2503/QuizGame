    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        try {
             
                String id=qid.getText();
                String que=questionField.getText();
                String o1=option1.getText();
                String o2=option2.getText();
                String o3=option3.getText();
                String o4=option4.getText();
                String a=ans.getText();
                //System.out.println("jiii");
                Class.forName("com.mysql.cj.jdbc.Driver");
                java.sql.Connection con=DriverManager.getConnection("jdbc:mysql://localhost/quizgame","root","1234");
    
                PreparedStatement pst=con.prepareStatement("update questions set question = ?,op1 = ?,op2 = ?,op3 = ?,op4 = ?,ans= ? where qid = ?"); 
               
                pst.setString(1,que);
                pst.setString(2,o1);
                pst.setString(3,o2);
                pst.setString(4,o3);
                pst.setString(5,o4);
                pst.setString(6,a);
                pst.setString(7,id);
                int count=pst.executeUpdate();
                System.out.println(count);
                if(count>0){
                    JOptionPane.showMessageDialog(this, "Question Updated Successfully");
                    this.setVisible(false);
                    new UpdateQuestion().setVisible(true);
                }
                else
                    JOptionPane.showMessageDialog(this, "Please add questions correctly");
                
            
        }
        catch(Exception e){
            System.out.println(e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateActionPerformed

